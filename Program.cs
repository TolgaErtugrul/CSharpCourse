//Değişkenler -> Variables

/*
- Değişkenler basit tabirle, verilerin geçici olarak depolandığı kısımlardır

- Veriler RAM (Random Access Memory) içerisinde depolanır.

Değişken tipleri

-Metinsel
    *char = ''
    *string = ""

-Sayısal
    -Tam Sayılar-
        *int = 2.1milyar( - , + )
        *long = 2.1milyar +
        *short = 32767
        *byte = 255

    -Ondalık Sayılar
        *float = 11.4F
        *double = 11.4
        *decimal = 11.4M

    -Mantıksal
        *bool = true ya da false

İsimlendirme : camelCasing

*/

char karakterDegiskeni= 'a';
char karakterDegiskeni2;
karakterDegiskeni2 = ',';

string metin = "Merhaba";

